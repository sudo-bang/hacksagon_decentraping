/**
 * Describes the complete site object returned by the /sitesdata API endpoint.
 * It includes the base site information plus its aggregated monitoring data.
 */
export interface SiteWithMonitoringData {
    // Fields from MonitoredSiteSchema
    _id: string; // Mongoose ObjectId is serialized as a string
    ownerId: string; // Mongoose ObjectId is serialized as a string
    url: string;
    name: string;
    isActive: boolean;
    lastStatus: 'PENDING' | 'UP' | 'DOWN';
    lastChecked: string | null; // Mongoose Date is serialized as an ISO string
    notifyByEmail: boolean;
    notifyBySms: boolean;
    createdAt: string; // Mongoose Date is serialized as an ISO string
    __v?: number; // Mongoose version key, typically included by .lean()

    // The added monitoring data object
    monitoringData: {
        /**
         * An array of the last 10 uptime statuses (true for up, false for down).
         * Padded with `null` at the beginning if fewer than 10 results exist.
         * The order is chronological (oldest to newest).
         * Example: [null, null, true, false, true, true, true, true, true, true]
         */
        uptime: (boolean | null)[];

        /**
         * An array of the last 10 page load times in milliseconds.
         * Padded with `null` at the beginning if fewer than 10 results exist.
         * The order is chronological (oldest to newest).
         */
        loadTime: (number | null)[];

        /**
         * An array of the last 10 SSL validation statuses (true for valid, false for invalid).
         * Padded with `null` at the beginning if fewer than 10 results exist.
         * The order is chronological (oldest to newest).
         */
        sslValid: (boolean | null)[];

        /**
         * The average uptime percentage over the available results, formatted to two decimal places.
         */
        averageUptime: number;

        /**
         * The average page load time in milliseconds over the available results, formatted to two decimal places.
         */
        averageLoadTime: number;
    };
}

